public class operator
{
	public static void main(String[] args)
	{
		char op='+';
		switch(op)
		{
			case '+':
			System.out.println("this is a operator");
			break;
			case '-':
			System.out.println("this is a operator");
			break;
			case '*':
			System.out.println("this is a operator");
			break;
			case '%':
			System.out.println("this is a operator");
			break;
			default:
			System.out.println("this is a not operator");
			break;
			
		}
	}
}